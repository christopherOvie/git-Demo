package practice;

public class Conditional {

	public static void main(String[] args) {
		int x=9;
		int y=10;
		
		if(x==y) {
			System.out.println("not sure");
		}
		else if(x>y) {
			System.out.println("correct");
		}
		else if(x<y) {
			System.out.println("inaccurate");
		}
		else {
			System.out.println("nonsense");
		}
		

	}

}
